-- Uma "view" em SQL é uma representação virtual
-- de uma tabela derivada de uma ou mais tabelas existentes.
-- Ela permite visualizar os dados selecionados de forma personalizada,
-- sem modificar a estrutura ou os dados subjacentes.
-- Em vez de armazenar dados fisicamente, uma view é uma consulta
-- salva no banco de dados que pode ser tratada como uma tabela normal.

